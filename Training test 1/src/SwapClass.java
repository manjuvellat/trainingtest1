
public class SwapClass {
	
		void swapfunc(int a, int b)
		{
			System.out.println("The numbers before swapping are a= " +a +" and b=" +b);
			int temp=a;
			a=b;
			b=temp;
			System.out.println("The numbers after swapping are a= " +a +" and b=" +b);
			
		}
		void swapfunc(String a, String b)
		{
			System.out.println("The strings before swapping are a= " +a +" and b=" +b);
			String temp=a;
			a=b;
			b=temp;
			System.out.println("The strings after swapping are a= " +a +" and b=" +b);
			
		}
	}


